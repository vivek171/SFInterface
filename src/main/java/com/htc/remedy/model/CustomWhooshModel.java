package com.htc.remedy.model;

import org.apache.lucene.search.Sort;

import java.util.Map;

public class CustomWhooshModel {

    String query;
    Sort sort;
    String[] requiredColumns;
    String sortfields;
    String sortorder;
    String distinct;
    String distinctfield;
    Map<String, String> fieldaliass;
    Map<String, String[]> params;
    Integer count;
    Integer pageno;


    public String getDistinctfield() {
        return distinctfield;
    }

    public void setDistinctfield(String distinctfield) {
        this.distinctfield = distinctfield;
    }

    public String getQuery() {
        return query;
    }

    public void setQuery(String query) {
        this.query = query;
    }

    public Sort getSort() {
        return sort;
    }

    public void setSort(Sort sort) {
        this.sort = sort;
    }

    public String[] getRequiredColumns() {
        return requiredColumns;
    }

    public void setRequiredColumns(String[] requiredColumns) {
        this.requiredColumns = requiredColumns;
    }

    public String getSortfields() {
        return sortfields;
    }

    public void setSortfields(String sortfields) {
        this.sortfields = sortfields;
    }

    public String getSortorder() {
        return sortorder;
    }

    public void setSortorder(String sortorder) {
        this.sortorder = sortorder;
    }

    public String getDistinct() {
        return distinct;
    }

    public void setDistinct(String distinct) {
        this.distinct = distinct;
    }

    public Map<String, String> getFieldaliass() {
        return fieldaliass;
    }

    public void setFieldaliass(Map<String, String> fieldaliass) {
        this.fieldaliass = fieldaliass;
    }

    public Map<String, String[]> getParams() {
        return params;
    }

    public void setParams(Map<String, String[]> params) {
        this.params = params;
    }

    public Integer getCount() {
        return count;
    }

    public void setCount(Integer count) {
        this.count = count;
    }

    public Integer getPageno() {
        return pageno;
    }

    public void setPageno(Integer pageno) {
        this.pageno = pageno;
    }
}
